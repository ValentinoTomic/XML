<div class="povijest"><p>Prva utrka Svjetskog prvenstva Formule 1 se održala 13. svibnja 1950. godine na stazi Silverstone u Velikoj Britaniji. Na toj utrci je pobijedio Giuseppe Farina u svojoj Alfetti 158 ispred druge dvije Alfe. Prvo prvenstvo je osvojio Talijan Giuseppe Farina u Alfi 1950., za dlaku pobijedivši svog argentinskog momčadskog vozača Juana Manuela Fangia. Ipak je Fangio osvojio naslov 1951. i još četiri sljedeća u narednih 6 godina dok mu nije dvostruki svjetski prvak Alberto Ascari poremetio planove vozeći za Ferrari. Iako se Britanac Stirling Moss redovito natjecao nikad nije uspio osvojiti Svjetsko prvenstvo. Fangio je ostao poznat po tome što je dominirao Formulom 1 čitavo desetljeće i dandanas se smatra vicemajstorom Formule 1.


Prvi Britanac koji je osvojio Svjetsko Prvenstvo je bio Mike Hawthorn, donijevši Ferrariju naslov 1958. M
eđutim kada je Colin Chapman ušao u F1 kao dizajner šasija i kasnije osnivač Lotusovih automobila, Velika Britanija je dominirala Formulom 1 cijelo desetljeće. Pored Jima Clarka, Jackieja Stewarta, Jacka Brabhama, Grahama Hilla i Dennyja Hulmea, britanski timovi i njihovi vozači su osvojili 12 svjetskih prvenstava od 1962. do 1973.

U sezoni 1962. uvedeno je novo vozilo s aluminijskom šasijom nazvanom monokok umjesto tradicionalne 
cjevaste šasije. 1968. Lotus je stavio reklamna slova Imperial Tobacca na vozilo i tako uveo sponzorstvo u taj sport.



Aerodinamika je polako dobila važnost u dizajniranju vozila s pojavom aerofoila krajem 1960. 
Krajem sedamdesetih Lotus je uveo zemljani efekt aerodinamike koji je bolidima osigurao ogroman downforce i uvelike povećao brzinu na zavojima.

Stvaranje Međunarodne federacije sportskih automobila 1979. je uzrokavalo niz trzavica između 
FISE i FOCE u kojima su se FISA i njen predsjednik Jean Marie Balestre neprestano sukobljavali s udrugom konstruktora Formule 1 oko televizijskih zarada i tehničkih pravila.

FIA je stavila zabranu na zemljani efekt aerodinamike u sezoni Formule 1 1983. Do tada su već 
turbonabijeni motori, koje je Renault uveo 1977., proizvodili 700 KS (520 kW) i tako postali neizostavni dio natjecanja. 
Kasnije, točnije 1987., turbo bolidi Formule proizvodili su 1000 KS u utrci. Ti automobili su bili i ostali najsnažniji trkaći 
automobili ikad napravljeni. Da bi smanjila energiju motora i brzinu, FIA je ograničila kapacitet spremnika goriva 1984. i smanjila brzine bolida 1988. 
prije zabrane turbonabijenih motora 1989.

Početkom devedesetih momčadi su uveli pomoćnog elektronskog vozača za upravljanje snagom motora, kontrolu protiv proklizavanja i poluautomatski mjenjač. 
Neke su posuđene od suvremenih cestovnih vozila. Aktivni ovjes je prvenstveno napravljen za stazu, a kasnije se probio na tržište osobnih automobila. 
FIA je zbog pritužbi da je tehnologija određivala rezultat utrka više nego vozačeva vještina zabranila 1994. mnogo takvih pomoćnih sredstava. 
Ipak mnogi promatrači su smatrali da je zabrana takvih pomoći vozačima zapravo bila zabrana zato što FIA nije imala neku tehnologiju koja bi eliminirala takve stvari u utrci.

Timovi su potpisali drugi dogovor o slaganju (Concorde Agreement), 1992. i treći 1997. koji je istekao krajem 2007.

Na stazi momčad McLaren i Wiliams su dominirali 80-tima i 90-tim. Honda i McLaren su najviše dominirali u 80-tima dok je Renault pojačan Wiliamsovim vozačima dobio nekoliko Svjetskih prvenstava sredinom 90-tih, sve dok se Mclaren nije vratio krajem 90-tih.

Rivalstvo između trkaćih legendi Senne i Prosta je postalo Formulina glavna zanimacija u sezoni 1988. i nastavila se sve dok se Prost nije povukao krajem 1993. Tragično i ironično, Senna je poginuo 1994. u zavoju Tamburelo na Velikoj nagradi San Marina i preuzeo je Prostovo vodstvo u Williamsu te godine. FIA je poboljšala sigurnosne standarde na stazi tako da od tog vikenda nijedan vozač nije poginuo za vrijeme utrke na stazi.

Vozači iz McLarena, Williamsa i Renaulta (prije poznatog kao Benetton) te Ferrarija (momčadi poznatije kao Velika četvorka) dobili su svako Svjetsko pravenstvo od 1984. do 2009.. Zbog tehnoloških napredaka u 90-tima cijena natjecanja u Formuli 1 je dramatično porasla. To je povećalo financijski teret zajedno s dominacijom te velike četvorke (koje su financirali veliki proizvođači automobila poput Daimler Chrysler, što je rezultiralo da male momčadi nisu financijski mogle opstati u Formuli 1. Financijski problemi su natjerali nekoliko timova da se povuku. Od 1990. 28 timova se povuklo iz Formule 1 što je ponukalo bivšeg Jordanovog vlasnika Eddieja Jordana da kaže da su dani natjecanja gusarskih brodova gotovi.
