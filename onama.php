<h2>Vremenska linija Formule 1</h2>
<p>1901. Prva upotreba riječi grand prix za opisivanje utrke, Velika nagrada Francuske u Le Mansu.

1904. Osnivanje Fédération Internationale de l'Automobile (FIA), neprofitne udruge osnovane da zastupa interese automobilskih organizacija i korisnika automobila.

1933. O startnim pozicijama na gridu odlučivalo je vrijeme kvalifikacija po prvi put na Velikoj nagradi Monaka.

1935. -39 . Održan je niz sastanaka s ciljem osnivanja svjetskog prvenstva u vožnji; oni su odloženi na policu pri izbijanju Drugog svjetskog rata.

1946. Formula 1 dogovorena je kao priznata formula.

1947. Ozvaničeno Svjetsko prvenstvo u vožnji. Do prve prvenstvene utrke trebale su proći još tri godine, iako je od ove godine bilo utrka po F1 propisima.

1950. Pokretanje svjetskog prvenstva za vozače s prvom utrkom na Velikoj nagradi Velike Britanije u Silverstoneu 13. svibnja. Iako su te godine bile 22 utrke bez prvenstva, samo šest se računalo za naslov. Prva službena F1 utrka, ne-prvenstvena utrka u Pauu, održana je prošlog mjeseca.

1952. Prvenstvo vozača odvijalo se prema propisima Formule 2 zbog zabrinutosti oko broja dostupnih bolida F1. To se nastavilo iu sezoni 1953.

1953. Argentina je bila domaćin prve velike nagrade prvenstva izvan Europe.

1954. Novi propisi F1, koji ograničavaju motore na 2,5 litre, rezultirali su vraćanjem svjetskog prvenstva prema propisima F1.

1958. Prvo prvenstvo konstruktora i prva velika nagrada Afrike (Maroko).

1958. Zabranjena je praksa dijeljenja automobila tijekom utrke.

Promjene pravila iz 1958. uključivale su uvođenje AvGas-a umjesto alkoholnih goriva i smanjenje duljine utrka s 500 km ili tri sata na 300 km ili dva sata.

1958. Stirling Moss pobijedio je u prvoj utrci u F1 bolidu sa stražnjim motorom. Unutar dvije godine svi su automobili imali ovaj dizajn

1961. Početak formule motora od 1,5 litara.

1962. Lotus je predstavio revolucionarni dizajn - automobil s aluminijskom monokok šasijom umjesto tradicionalnog dizajna prostornog okvira, najavljen kao jedan od najznačajnijih tehnoloških otkrića.

1966. Početak formule 3-litrenog motora.

1967. Velika nagrada Njemačke bila je prva koja se prenosila u boji.

1968 Lotus automobili nosili su logotipe Imperial Tobacco na svojim automobilima, najavljujući dolazak sponzorstva.

1970. Jochen Rindt na Velikoj nagradi Italije, a kasnije postaje prvi i jedini posthumni F1 prvak.

1973. Pace car je prvi put korišten tijekom Velike nagrade Kanade.

1976. Prva velika nagrada održana je u Aziji (Japan). Tyrrell je predstavio automobil sa šest kotača.

1977. Renault je proizveo prvi automobil s turbopunjačem. Na kraju su zabranjeni 1989.

1978. Bernie Ecclestone postao je predsjednik Udruge konstruktora Formule 1 (FOCA). Uvođenje medicinskog automobila za praćenje automobila u formacijskom krugu svake velike nagrade s ciljem poboljšanja vremena reakcije u slučaju nezgode u prvom krugu. Prilikom prvog pojavljivanja udario je u rubnik i odletio u zrak.

1979. Formiranje Fédération Internationale du Sport Automobile (FISA) kao upravnog tijela za natjecanja.

1982. Lotus je predstavio novi sustav aktivnog ovjesa, signalizirajući početak porasta elektroničkih pomagala za vozača. Uoči VN Južne Afrike održan je štrajk vozača protiv uvođenja vozačkih dozvola.

1983. Održana je posljednja F1 utrka bez prvenstva.

1984. Velika nagrada Austrije bila je prva na kojoj su sudjelovali samo automobili s turbopunjačem.

1985. Prvo svjetsko prvenstvo Grand Prix održano je u Oceaniji (Australija).

1990. Adelaide je bio domaćin 500. Grand Prixa.

1992. Prvo pojavljivanje sigurnosnog automobila na Velikoj nagradi Velike Britanije.

1994. Ayrton Senna je poginuo na Velikoj nagradi San Marina, dan nakon što je Roland Ratzenberger također izgubio život u nesreći tijekom kvalifikacija. Tragedije su pokrenule težnju za poboljšanjem sigurnosnih standarda i bili su posljednji vozači koji su poginuli za volanom F1 bolida.

1998. Uvedeni novi propisi u vezi s užim tragovima i gumama s utorima.

2002. Timski nalozi, legalni od 1950., zabranjeni su nakon nekoliko incidenata u kojima su rezultati utrke otvoreno manipulirani.

2008. Osnovana je Udruga momčadi Formule 1 (FOTA), organizacija koja zastupa interese momčadi.</p>
